/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
    int m; 
    int shift = 33+~n; //32-n
    m = x << shift; //Shift left
    m = m >> shift; //Shift right
    
  return !(m ^ x); //Same number if zero then return 1.
}
/* 
 * divpwr2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: divpwr2(15,1) = 7, divpwr2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int divpwr2(int x, int n) {
    
    int mask = 0x80; //Mask to extract sign bit
    int sign;
    
    mask = mask << 24; //Get sign to know if bias is needed.
    sign = mask & x; //Get sign.
    sign = !(!sign); //0 if pos 1 if neg
    sign = sign & (!(!n)); //Are we dividing by 1.
    
    x = (x + (sign << n) + (~sign+1)) >> n; //Bias
       
    return (x);
}
