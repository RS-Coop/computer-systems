/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
    int mask = 0xff; //Mask of 0000...1111 1111
    
    return ((x >> (n << 3))& mask);//Shift x right by n*8 and mask it
}
/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n) {
    
    int mask = 0x80; //Mask to extract sign bit
    int sx;
    
    mask = mask << 24;
    
    sx = (mask & x); //Extract sign bit
    x = x & ~mask; //Remove the sign bit. Need this because we do x|sign.
    
    sx = sx >> 1; //Shift bit one could be 0000... or 1100...
    
    sx = sx & (0x80 << 23); //Remove potential leading 1
    
    sx = sx >> 30; //Shift to end, now is ...0001
    sx = sx << (32+~n); //Shift left by 31-n bits
    
    x = x >> n; //Right shift x.
    
    x = x | sx;//Replace sign bit
    
    return (x);
}
/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
    
    int y1, bits;
    int mask = 0x11; //Mask like 0001 0001
    mask = mask | (mask << 8) | (mask << 16) | (mask << 24); //Extend mask to 32 bits.
    
    y1 = mask & x; //Extract all right bits
    y1 += (x >> 1) & mask; //Shift x and extract all second to right
    y1 += (x >> 2) & mask; //Shift x and extract all third to right
    y1 += (x >> 3) & mask; //Shift x and extract all left bits.
    
    //Shift y mask it and add it to total.
    bits = (0xf & y1);
    bits += (0xf & (y1 >> 4));
    bits += (0xf & (y1 >> 8));
    bits += (0xf & (y1 >> 12));
    bits += (0xf & (y1 >> 16));
    bits += (0xf & (y1 >> 20));
    bits += (0xf & (y1 >> 24));
    bits += (0xf & (y1 >> 28));
    
    
  return bits;
}
