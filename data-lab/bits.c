/* 
 * isPositive - return 1 if x > 0, return 0 otherwise 
 *   Example: isPositive(-1) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 3
 */
int isPositive(int x) {
    
    int mask = 0x80; //Mask to extract sign bit
    int sign, same;
    
    mask = mask << 24; 
    sign = mask & x; //Get sign
    sign = sign >> 31;
    sign = sign & 1; //1 or 0
    
    same = !(x^0); //Is x zero

  return !(sign | same);
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y) {
    int signMask = 0x80; //Mask to extract sign bit
    int sx, sy, ss, truth;
    
    signMask = signMask << 24;
    sx = !(signMask & x);
    sy = !(signMask & y);
    ss = !(signMask & (y+(~x+1)));
    
    truth = (sx & sy & ss) | (!sx & !sy & ss) | (!sx & sy);
    
  return truth;
}
